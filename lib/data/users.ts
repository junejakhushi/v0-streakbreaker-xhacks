export const getUserById = (id: string): User | undefined => {
  if (id === 'current-user') return currentDemoUser;
  return mockUsers.find(user => user.id === id);
};

export const getFriendsForUser = (userId: string): User[] => {
  const user = getUserById(userId);
  if (!user) return [];
  return user.friends.map(friendId => getUserById(friendId)).filter(Boolean) as User[];
};

export const getRandomFriend = (userId: string): User | undefined => {
  const friends = getFriendsForUser(userId);
  return friends[Math.floor(Math.random() * friends.length)];
};
