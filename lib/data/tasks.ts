export const getTasksByRealm = (realm: Realm): Task[] => {
  return tasks.filter(task => task.realm === realm);
};

export const getTasksByVibe = (vibe: Vibe): Task[] => {
  return tasks.filter(task => task.vibe === vibe);
};

export const getRandomTask = (realm: Realm, vibe?: Vibe): Task => {
  let filtered = getTasksByRealm(realm);
  if (vibe) {
    filtered = filtered.filter(task => task.vibe === vibe);
  }
  return filtered[Math.floor(Math.random() * filtered.length)];
};

export const getFriendPickOptions = (realm: Realm): Task[] => {
  const realmTasks = getTasksByRealm(realm).filter(t => t.canBeFriendPicked);
  const shuffled = [...realmTasks].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 3);
};

